// Translation helper function
function updateTranslations() {
  const lang = document.getElementById("languageSelect").value;
  const t = translations[lang];
  
  // Update all text elements
  document.getElementById("titleDigitalClock").textContent = t.digitalClock;
  document.getElementById("setAlarmBtn").textContent = t.setAlarm;
  document.getElementById("testAlarmBtn").textContent = t.testAlarm;
  document.getElementById("stopAlarmSoundBtn").textContent = t.stopAlarmSound;
  document.getElementById("labelTimeZone").textContent = t.timeZoneLabel;
  document.getElementById("labelLanguage").textContent = t.languageLabel;
  document.getElementById("titleWorldTime").textContent = t.worldTime;
  document.getElementById("titleChronometer").textContent = t.chronometer;
  document.getElementById("startChronoBtn").textContent = t.start;
  document.getElementById("stopChronoBtn").textContent = t.stop;
  document.getElementById("resetChronoBtn").textContent = t.reset;
  document.getElementById("titleTutorial").textContent = t.tutorial;
  document.getElementById("tutorialText").textContent = t.tutorialText;
  document.getElementById("titleTimer").textContent = t.timer;
  document.getElementById("setTimerBtn").textContent = t.setTimer;
  document.getElementById("startTimerBtn").textContent = t.startTimer;
  document.getElementById("stopTimerBtn").textContent = t.stopTimer;
  document.getElementById("resetTimerBtn").textContent = t.resetTimer;
  document.getElementById("stopTimerSoundBtn").textContent = t.stopTimerSound;
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Update any other dynamic content
  updateAlarmListTranslations();
}

// Update alarm list with current language
function updateAlarmListTranslations() {
  const deleteButtons = document.querySelectorAll('.deleteAlarmBtn');
  const lang = document.getElementById("languageSelect").value;
  
  deleteButtons.forEach(button => {
    const alarmTime = button.parentElement.textContent.split(' ')[0];
    button.setAttribute('aria-label', `${translations[lang].deleteAlarm} ${alarmTime}`);
  });
}

// Get translated message
function getTranslation(key) {
  const lang = document.getElementById("languageSelect").value;
  return translations[lang][key] || translations['en'][key];
}

// Export for use in other modules
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, updateTranslations, getTranslation };
}
    